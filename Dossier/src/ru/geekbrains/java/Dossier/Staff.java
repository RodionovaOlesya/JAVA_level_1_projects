package ru.geekbrains.java.Dossier;

public class Staff {
    String name;
    String position;
    String email;
    String telNum;
    int salary;
    int age;

       public Staff(String name, String position, String email, String telNum, int salary, int age) {
        this.name = name;
        this.position = position;
        this.email = email;
        this.telNum = telNum;
        this.salary = salary;
        this.age = age;
    }

}
